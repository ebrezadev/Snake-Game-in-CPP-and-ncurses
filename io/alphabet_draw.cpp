void DrawString::drawString(char *str, int start_x, int start_y)
{
	int x = start_x;
	int y = start_y;

	for (int i = 0; str[i] != '\0'; i++)
	{
		for (int j = 0; j < 5; j++)
		{
			for (int k = 0; k < 5; k++)
			{
				if (alphabet_table[str[i] - ' '][k][j] == true)
				{
					mvwaddch(stdscr, y, x, ACS_CKBOARD);
				}

				y++;
			}

			x++;
			y = start_y;
		}

		x++;
	}
}